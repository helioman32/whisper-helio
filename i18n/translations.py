# ── Langue active & cache ─────────────────────────────────────────────────
_lang_provider = None   # callable → str ("fr", "en", "de")
_tr_cache = {}          # {(lang, key): text} — vidé au changement de langue


def set_lang_provider(provider):
    """Configure le fournisseur de langue active (callable → str).
    Appelé une seule fois depuis dictee.pyw au démarrage."""
    global _lang_provider
    _lang_provider = provider


def clear_cache():
    """Vide le cache des traductions (appeler après changement de langue)."""
    _tr_cache.clear()


def tr(key, **kwargs):
    """Retourne la traduction pour la clé donnée (avec cache pour les appels sans kwargs)."""
    lang = _lang_provider() if _lang_provider else "fr"
    if not kwargs:
        cached = _tr_cache.get((lang, key))
        if cached is not None:
            return cached
    t = TRANSLATIONS.get(lang, TRANSLATIONS["fr"])
    text = t.get(key, TRANSLATIONS["fr"].get(key, key))
    if not kwargs:
        _tr_cache[(lang, key)] = text
        return text
    return text.format_map(kwargs)
